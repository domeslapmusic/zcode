<body><p>ZCode es el sitio en el que guardo mi c&oacute;digo y tambi&eacute;n un peque&ntilde;o framework para AS2.</p>
<h2>El framework</h2>
<p>El framework consiste s&oacute;lo en 3 clases "principales" (modelo,
vista y objeto de configuraci&oacute;n) mas unas cuantas
clases&nbsp;"helpers"
(acceso a FlashVars, manejo del bot&oacute;n derecho,
etc).</p>
<p>La idea principal detr&aacute;s de ZCode es que deber&iacute;a ser
la base para tu propio framework. &Eacute;chale un ojo, utiliza lo que
te parezca interesante y deja lo dem&aacute;s. Aunque es
plenamente funcional, es a prop&oacute;sito *muy* sencillo. Espero que
te ayude con el concepto de framework y que te animes a desarrollar el
tuyo propio.</p>
<p>Como yo uso&nbsp;<a href="http://mtasc.org">MTASC</a> a diario, el
c&oacute;digo compila sin problemas con &eacute;l. De hecho, si
compilas con el IDE de Flash puede que te salten algunos avisos. Por
ejemplo, al compilador oficial no le gusta que haya c&oacute;digo en el
constructor antes de llamar a <span style="font-style: italic;">super</span>.
A veces suelta un aviso (a veces no) pero aun as&iacute; compila. Si
trabajas con AS2, date un lujo y &eacute;chale un ojo a MTASC o <a href="http://osflash.org/projects/flasc">FLASC</a>
(Flash IDE + MTASC).</p>
<p>Si quieres documentaci&oacute;n he escrito un <a href="<?php echo base_url(); ?>assets/projects/zcode/zcode.pdf">peque&ntilde;o documento</a>
explicando por qu&eacute; desarrollar y usar un framework propio es
interesante. Tambi&eacute;n explica c&oacute;mo empezar a usar
ZCode.&nbsp;</p>
<p>Para comenzar, simplemente p&aacute;sate por el&nbsp;<a href="http://zcode.googlecode.com/svn/trunk/as2/tv/zarate/projects/demo/zcode/">Hello
World</a> y &eacute;chale un ojo al c&oacute;digo. Con un poco de suerte no necesita mayor explicaci&oacute;n.</p>
